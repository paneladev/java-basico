package paneladev.arrays;

public class Arrays {
    public static void main(String[] args) {

        String[] nomes = {"Ford", "Chevrolet", "Fiat"};
        for(String nome : nomes) {
            System.out.println(nome);
        }

        int[] valores = {10,20,30,40,50};
        for(int val : valores) {
            System.out.println(val);
        }
    }
}
